    public Payment(CustomerRepository customerRepository, double amount) {
        initComponents();
        this.customerRepository = customerRepository;
        this.amount = amount;
        
        this.labelChange.setText(Double.toString(-amount));

        // Listen for changes in the text
        textNit.getDocument().addDocumentListener(new DocumentListener() {
            @Override
            public void changedUpdate(DocumentEvent e) {
                searchCustomer();
            }

            @Override
            public void removeUpdate(DocumentEvent e) {
                searchCustomer();
            }

            @Override
            public void insertUpdate(DocumentEvent e) {
                searchCustomer();
            }

            public void searchCustomer() {
                Optional<Customer> customer = customerRepository.get(textNit.getText());

                textName.setText(customer.isPresent() ? customer.get().getName() : "");
                System.out.print(textNit.getText());
            }
        });

        // Listen for changes in the text
        textDisbursement.getDocument().addDocumentListener(new DocumentListener() {
            @Override
            public void changedUpdate(DocumentEvent e) {
                updateChange();
            }

            @Override
            public void removeUpdate(DocumentEvent e) {
                updateChange();
            }

            @Override
            public void insertUpdate(DocumentEvent e) {
                updateChange();
            }
        });
    }

    private void updateChange() {
        String input = this.textDisbursement.getText();
        double value = Double.parseDouble(input.isBlank() ? "0" :  input) - amount;
        labelChange.setText(Double.toString(value));
    }

